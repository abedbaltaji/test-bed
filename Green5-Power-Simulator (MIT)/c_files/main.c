uint8_t simpleserial_recv_byte() {
    volatile uint32_t* status = (uint32_t*)(STDIN_BASEADDRESS + XUL_STATUS_REG_OFFSET);
    volatile uint32_t* data = (uint32_t*)(STDIN_BASEADDRESS + XUL_RX_FIFO_OFFSET);
    // Wait untill there is data
    while ((*status & XUL_SR_RX_FIFO_VALID_DATA) != XUL_SR_RX_FIFO_VALID_DATA) {}
    // Pop data
    return (uint8_t)(*data);
}

void simpleserial_send_byte(uint8_t val) {
    volatile uint32_t* status = (uint32_t*)(STDOUT_BASEADDRESS + XUL_STATUS_REG_OFFSET);
    volatile uint32_t* data = (uint32_t*)(STDOUT_BASEADDRESS + XUL_TX_FIFO_OFFSET);
    // Wait while the queue is full
    while ((*status & XUL_SR_TX_FIFO_FULL) == XUL_SR_TX_FIFO_FULL) {}
    // Push data
    *data = (uint32_t)val;
}

void simpleserial_send_bytes(uint32_t len, const uint8_t* data) {
	for(uint32_t i = 0; i < len; i++) {
		simpleserial_send_byte(data[i]);
	}
}

void simpleserial_recv_bytes(uint32_t len, uint8_t* data) {
	for(uint32_t i = 0; i < len; i++) {
		data[i] = simpleserial_recv_byte();
	}
}


